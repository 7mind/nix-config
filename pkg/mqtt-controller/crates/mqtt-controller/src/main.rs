//! `mqtt-controller` binary entry point. Two subcommands:
//!
//!   * `provision` — one-shot reconciler. Replaces `hue-setup`.
//!   * `daemon`    — long-running runtime controller. Replaces the bento
//!                   `mqtt-automation` systemd service.

use std::path::PathBuf;
use std::sync::Arc;
use std::time::Duration;

use anyhow::{Context, Result};
use clap::{Parser, Subcommand};
use tracing_subscriber::layer::SubscriberExt;
use tracing_subscriber::util::SubscriberInitExt;
use tracing_subscriber::{EnvFilter, Layer};

use mqtt_controller::config::Config;
use mqtt_controller::mqtt::MqttConfig;
use mqtt_controller::provision::{ProvisionOptions, reconcile};
use mqtt_controller::time::SystemClock;
use mqtt_controller::web::decision_capture::DecisionCaptureLayer;

#[derive(Debug, Parser)]
#[command(
    name = "mqtt-controller",
    version,
    about = "Unified zigbee2mqtt provisioner and runtime controller"
)]
struct Cli {
    /// Verbose action logging. When set, every command the daemon
    /// publishes to MQTT (and every state-machine branch that decides
    /// to publish one) is logged at info level with a human-readable
    /// reason. Without this flag the daemon only emits
    /// warnings and errors.
    ///
    /// Equivalent to setting `RUST_LOG=mqtt_controller=info`. The env
    /// var still wins if set explicitly.
    #[arg(long, short = 'v', global = true)]
    verbose: bool,

    #[command(subcommand)]
    command: Command,
}

#[derive(Debug, Subcommand)]
enum Command {
    /// Reconcile z2m groups, members, scenes, and per-device options
    /// against the JSON config. One-shot. Replaces `hue-setup`.
    Provision(ProvisionArgs),

    /// Long-running runtime controller. Subscribes to button/motion/group
    /// MQTT events, runs the per-zone state machine, publishes commands.
    /// Replaces the bento `mqtt-automation` service.
    Daemon(DaemonArgs),
}

#[derive(Debug, Parser)]
struct CommonMqttArgs {
    /// Path to the unified JSON config (generated by Nix from
    /// `defineRooms`).
    #[arg(long)]
    config: PathBuf,

    #[arg(long, default_value = "localhost")]
    mqtt_host: String,

    #[arg(long, default_value_t = 1883)]
    mqtt_port: u16,

    #[arg(long, default_value = "mqtt")]
    mqtt_user: String,

    /// File containing the MQTT password (one line, trailing whitespace
    /// stripped).
    #[arg(long)]
    mqtt_password_file: PathBuf,
}

#[derive(Debug, Parser)]
struct ProvisionArgs {
    #[command(flatten)]
    mqtt: CommonMqttArgs,

    /// Print what would be done without publishing anything.
    #[arg(long)]
    dry_run: bool,

    /// Re-issue scene_add for every configured scene even if a scene with
    /// the same id and name already exists.
    #[arg(long)]
    force_update: bool,

    /// Remove members and groups present in z2m but not in the config.
    /// Default is additive-only.
    #[arg(long)]
    prune: bool,

    /// MQTT operation timeout, seconds.
    #[arg(long, default_value_t = 5.0)]
    timeout: f64,

    /// How many times to retry the initial bridge/groups + bridge/devices
    /// fetch on early-boot races (z2m up but inventory not yet published).
    #[arg(long, default_value_t = 12)]
    fetch_attempts: u32,

    /// Delay between fetch retries, seconds.
    #[arg(long, default_value_t = 5.0)]
    fetch_retry_seconds: f64,

    /// z2m WebSocket API URL for bulk device state fetch.
    /// Example: ws://localhost:8080/api
    #[arg(long)]
    z2m_ws_url: String,
}

#[derive(Debug, Parser)]
struct DaemonArgs {
    #[command(flatten)]
    mqtt: CommonMqttArgs,

    /// IANA timezone name for time-of-day slot dispatch (e.g.
    /// "Europe/Amsterdam"). Defaults to the system's TZ environment
    /// variable, falling back to UTC.
    #[arg(long)]
    timezone: Option<String>,

    /// Port for the web dashboard HTTP/WebSocket server. If omitted, no
    /// web server starts and the daemon runs MQTT-only as before.
    #[arg(long)]
    web_port: Option<u16>,

    /// Directory containing the pre-built WASM frontend assets
    /// (index.html + *.wasm + *.js). Required when --web-port is set.
    #[arg(long)]
    web_assets_dir: Option<PathBuf>,
}

/// Initialize the tracing subscriber.
///
/// Precedence (highest first):
///   1. `RUST_LOG` env var (always wins if set)
///   2. `--verbose` CLI flag → `mqtt_controller=info` (action logging on)
///   3. default → `mqtt_controller=warn` (only warnings + errors)
///
/// When `web_enabled` is true, installs a [`DecisionCaptureLayer`]
/// alongside the fmt layer so that `start_capture()`/`drain_capture()`
/// can collect decision tracing during `handle_event`.
fn init_tracing(verbose: bool, web_enabled: bool) {
    let filter = EnvFilter::try_from_default_env().unwrap_or_else(|_| {
        if verbose {
            EnvFilter::new("mqtt_controller=info")
        } else {
            EnvFilter::new("mqtt_controller=warn")
        }
    });
    let fmt_layer = tracing_subscriber::fmt::layer().with_filter(filter);

    if web_enabled {
        tracing_subscriber::registry()
            .with(fmt_layer)
            .with(DecisionCaptureLayer)
            .init();
    } else {
        tracing_subscriber::registry()
            .with(fmt_layer)
            .init();
    }
}

fn main() -> Result<()> {
    let cli = Cli::parse();
    let web_enabled = matches!(&cli.command, Command::Daemon(d) if d.web_port.is_some());
    init_tracing(cli.verbose, web_enabled);

    let runtime = tokio::runtime::Builder::new_multi_thread()
        .enable_all()
        .build()
        .context("building tokio runtime")?;

    runtime.block_on(async {
        match cli.command {
            Command::Provision(args) => run_provision(args).await,
            Command::Daemon(args) => run_daemon(args).await,
        }
    })
}

fn read_password(path: &PathBuf) -> Result<String> {
    let raw = std::fs::read_to_string(path)
        .with_context(|| format!("reading mqtt password file {}", path.display()))?;
    Ok(raw.trim().to_string())
}

fn build_mqtt_config(args: &CommonMqttArgs, role: &str) -> Result<MqttConfig> {
    let password = read_password(&args.mqtt_password_file)?;
    let client_id = format!("mqtt-controller-{role}-{}", uuid::Uuid::new_v4());
    Ok(MqttConfig::new(
        args.mqtt_host.clone(),
        args.mqtt_port,
        args.mqtt_user.clone(),
        password,
        client_id,
    ))
}

async fn run_provision(args: ProvisionArgs) -> Result<()> {
    let config = Config::load_from_path(&args.mqtt.config)
        .with_context(|| format!("loading config from {}", args.mqtt.config.display()))?;
    tracing::info!(
        rooms = config.rooms.len(),
        devices = config.devices.len(),
        dry_run = args.dry_run,
        force_update = args.force_update,
        prune = args.prune,
        "loaded config (provision)"
    );

    let mqtt = build_mqtt_config(&args.mqtt, "provision")?;
    let options = ProvisionOptions {
        dry_run: args.dry_run,
        force_update: args.force_update,
        prune: args.prune,
        timeout: Duration::from_secs_f64(args.timeout),
        settle: Duration::from_millis(400),
        fetch_attempts: args.fetch_attempts,
        fetch_retry: Duration::from_secs_f64(args.fetch_retry_seconds),
    };

    let summary = reconcile(&config, mqtt, &args.z2m_ws_url, options)
        .await
        .context("reconciling z2m state")?;

    let action_word = if args.dry_run { "would touch" } else { "touched" };
    tracing::info!(
        touched = summary.touched,
        skipped = summary.skipped,
        "done; {action_word} {} item(s)",
        summary.touched
    );
    Ok(())
}

async fn run_daemon(args: DaemonArgs) -> Result<()> {
    let config = Config::load_from_path(&args.mqtt.config)
        .with_context(|| format!("loading config from {}", args.mqtt.config.display()))?;
    tracing::info!(rooms = config.rooms.len(), "loaded config (daemon)");

    let timezone = resolve_timezone(args.timezone.as_deref())?;
    let clock = Arc::new(SystemClock::new(timezone));
    let mqtt = build_mqtt_config(&args.mqtt, "daemon")?;

    let web = if let Some(port) = args.web_port {
        let assets_dir = args
            .web_assets_dir
            .context("--web-assets-dir is required when --web-port is set")?;
        anyhow::ensure!(
            assets_dir.is_dir(),
            "--web-assets-dir {}: not a directory",
            assets_dir.display()
        );

        let (ws_cmd_tx, ws_cmd_rx) = tokio::sync::mpsc::channel(64);
        let (broadcast_tx, _) = tokio::sync::broadcast::channel(256);

        let addr = std::net::SocketAddr::from(([0, 0, 0, 0], port));
        let (_bound_addr, _server_handle) =
            mqtt_controller::web::bind_and_start_web_server(
                addr,
                ws_cmd_tx,
                broadcast_tx.clone(),
                assets_dir,
            )
            .await
            .with_context(|| format!("binding web server on {addr}"))?;

        Some(mqtt_controller::web::WebHandle {
            ws_cmd_rx,
            broadcast_tx,
        })
    } else {
        None
    };

    mqtt_controller::daemon::run(config, mqtt, clock, web).await
}

fn resolve_timezone(explicit: Option<&str>) -> Result<chrono_tz::Tz> {
    let name = explicit
        .map(String::from)
        .or_else(|| std::env::var("TZ").ok())
        .unwrap_or_else(|| "UTC".to_string());
    name.parse::<chrono_tz::Tz>()
        .map_err(|e| anyhow::anyhow!("invalid timezone {name:?}: {e}"))
}
